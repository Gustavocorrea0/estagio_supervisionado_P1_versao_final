package Maquina;

public class Maquina {
    private int idMaquina;
    private String tipoMaquina;
    private String nomeMaquina;
    private String marcaMaquina;
    private int anoMaquina;
    private String chassiMaquina;
    private String estadoMaquina;

    public int getIdMaquina() {
        return idMaquina;
    }

    public void setIdMaquina(int idMaquina) {
        this.idMaquina = idMaquina;
    }

    public String getTipoMaquina() {
        return tipoMaquina;
    }

    public void setTipoMaquina(String tipoMaquina) {
        this.tipoMaquina = tipoMaquina;
    }

    public String getNomeMaquina() {
        return nomeMaquina;
    }

    public void setNomeMaquina(String nomeMaquina) {
        this.nomeMaquina = nomeMaquina;
    }

    public String getMarcaMaquina() {
        return marcaMaquina;
    }

    public void setMarcaMaquina(String marcaMaquina) {
        this.marcaMaquina = marcaMaquina;
    }

    public int getAnoMaquina() {
        return anoMaquina;
    }

    public void setAnoMaquina(int anoMaquina) {
        this.anoMaquina = anoMaquina;
    }

    public String getChassiMaquina() {
        return chassiMaquina;
    }

    public void setChassiMaquina(String chassiMaquina) {
        this.chassiMaquina = chassiMaquina;
    }

    public String getEstadoMaquina() {
        return estadoMaquina;
    }

    public void setEstadoMaquina(String estadoMaquina) {
        this.estadoMaquina = estadoMaquina;
    }
    
    
}
