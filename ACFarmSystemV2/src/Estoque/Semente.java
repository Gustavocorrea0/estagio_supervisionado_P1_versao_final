package Estoque;

public class Semente {
    private int idSemente;
    private String nomeSemente;
    private String marcaSemente;
    private Double quantidadeEmSacos;
    private Double quantidadeMinimaEmSacos;

    public int getIdSemente() {
        return idSemente;
    }

    public void setIdSemente(int idSemente) {
        this.idSemente = idSemente;
    }

    public String getNomeSemente() {
        return nomeSemente;
    }

    public void setNomeSemente(String nomeSemente) {
        this.nomeSemente = nomeSemente;
    }

    public String getMarcaSemente() {
        return marcaSemente;
    }

    public void setMarcaSemente(String marcaSemente) {
        this.marcaSemente = marcaSemente;
    }

    public Double getQuantidadeEmSacos() {
        return quantidadeEmSacos;
    }

    public void setQuantidadeEmSacos(Double quantidadeEmSacos) {
        this.quantidadeEmSacos = quantidadeEmSacos;
    }

    public Double getQuantidadeMinimaEmSacos() {
        return quantidadeMinimaEmSacos;
    }

    public void setQuantidadeMinimaEmSacos(Double quantidadeMinimaEmSacos) {
        this.quantidadeMinimaEmSacos = quantidadeMinimaEmSacos;
    }
    
    
}
