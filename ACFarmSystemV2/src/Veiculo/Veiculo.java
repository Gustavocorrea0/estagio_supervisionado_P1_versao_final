package Veiculo;

/**
 *
 * @author Gustavo
 */
public class Veiculo {
    
    private Long idVeiculo;
    private String tipoVeiculo;
    private String nomeVeiculo;
    private String marcaVeiculo;
    private int anoVeiculo;
    private String chassiVeiculo;
    private String estadoVeiculo;
    private Double capacidadeDoTanqueDeCombustivel;
    

    public Long getIdVeiculo() {
        return idVeiculo;
    }

    public void setIdVeiculo(Long idVeiculo) {
        this.idVeiculo = idVeiculo;
    }


    public String getTipoVeiculo() {
        return tipoVeiculo;
    }

    public void setTipoVeiculo(String tipoVeiculo) {
        this.tipoVeiculo = tipoVeiculo;
    }

    public String getNomeVeiculo() {
        return nomeVeiculo;
    }

    public void setNomeVeiculo(String nomeVeiculo) {
        this.nomeVeiculo = nomeVeiculo;
    }

    public String getMarcaVeiculo() {
        return marcaVeiculo;
    }

    public void setMarcaVeiculo(String marcaVeiculo) {
        this.marcaVeiculo = marcaVeiculo;
    }

    public int getAnoVeiculo() {
        return anoVeiculo;
    }

    public void setAnoVeiculo(int anoVeiculo) {
        this.anoVeiculo = anoVeiculo;
    }

    public String getChassiVeiculo() {
        return chassiVeiculo;
    }

    public void setChassiVeiculo(String chassiVeiculo) {
        this.chassiVeiculo = chassiVeiculo;
    }

    public String getEstadoVeiculo() {
        return estadoVeiculo;
    }

    public void setEstadoVeiculo(String estadoVeiculo) {
        this.estadoVeiculo = estadoVeiculo;
    }

    public Double getCapacidadeDoTanqueDeCombustivel() {
        return capacidadeDoTanqueDeCombustivel;
    }

    public void setCapacidadeDoTanqueDeCombustivel(Double capacidadeDoTanqueDeCombustivel) {
        this.capacidadeDoTanqueDeCombustivel = capacidadeDoTanqueDeCombustivel;
    }


}
