package Propriedade;

/**
 *
 * @author Gustavo
 */
public class Propriedade {
    private int idPropriedade;
    private String nomePropriedade;
    private String localPropriedade;
    private Double areaPropriedade;
    private String tipoDeCulturaDaPropriedade;
    private String estado;
    private String cidade;

    public int getIdPropriedade() {
        return idPropriedade;
    }

    public void setIdPropriedade(int idPropriedade) {
        this.idPropriedade = idPropriedade;
    }

    public String getNomePropriedade() {
        return nomePropriedade;
    }

    public void setNomePropriedade(String nomePropriedade) {
        this.nomePropriedade = nomePropriedade;
    }

    public String getLocalPropriedade() {
        return localPropriedade;
    }

    public void setLocalPropriedade(String localPropriedade) {
        this.localPropriedade = localPropriedade;
    }

    public Double getAreaPropriedade() {
        return areaPropriedade;
    }

    public void setAreaPropriedade(Double areaPropriedade) {
        this.areaPropriedade = areaPropriedade;
    }

    public String getTipoDeCulturaDaPropriedade() {
        return tipoDeCulturaDaPropriedade;
    }

    public void setTipoDeCulturaDaPropriedade(String tipoDeCulturaDaPropriedade) {
        this.tipoDeCulturaDaPropriedade = tipoDeCulturaDaPropriedade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }
    
    
}
